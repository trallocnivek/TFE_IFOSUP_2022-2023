<h1>Politique d'utilisation des cookies</h1>
<h2>Information sur notre usage des cookies</h2>
<p>
	Notre site web fait usage de cookies et autres technologies similaires pour identifier vos préférences d’utilisation par rapport à celles des autres utilisateurs de notre site internet. Ceci nous aide à vous offrir une meilleure expérience d’utilisation lorsque vous naviguez sur notre site internet, et nous permet d’optimaliser celui-ci.
</p>
<p>
	Les cookies et technologies similaires ne permettent pas de récolter systématiquement des informations pouvant identifier les utilisateurs de notre site internet. Ils nous aident seulement à améliorer le fonctionnement de notre site, à comprendre les intérêts de nos utilisateurs et à mesurer l’efficacité du contenu de notre site web.
</p>
<p>
	Suite aux récentes modifications de la législation, tous les sites internet s’adressant à certaines parties de l’Union européenne sont obligés de vous demander l’autorisation pour l’usage et l’enregistrement de cookies et autres technologies similaires sur vos ordinateurs et appareils mobiles. Cette Politique de cookies vous informe clairement et de manière exhaustive sur les cookies que nous utilisons et leur objectif. 
</p>
<p>
	Veuillez lire notre <a href="?page=conditions">Politique de confidentialité</a> afin de prendre connaissance des règles de protections de la vie privée qui sont d’application sur ce site internet. Pour obtenir plus d’informations sur cette Politique de cookies, vous pouvez toujours nous contacter par e-mail : <a href="mailto:contact@swingshift-examen.be">contact@swingshift-examen.be</a>.
</p>
<h2>Quesqu'un cookie ?</h2>
<p>
	Les cookies sont de « mini fichiers » qui peuvent être placés sur l’appareil d’un utilisateur connecté à Internet, comme un ordinateur, un téléphone, une tablette ou encore une télévision intelligente. Les cookies peuvent être utilisés pour recueillir ou stocker des informations sur la manière dont l’utilisateur se comporte sur (un site) Internet et/ou sur son appareil. La « lecture » de ces cookies permet ensuite aux sites web qui les ont placés de récupérer les informations qui y sont stockées.
</p>
<p>
	Pour plus d'informations veuillez consulter ces pages : <a href="https://www.autoriteprotectiondonnees.be/professionnel/themes/cookies">https://www.autoriteprotectiondonnees.be/professionnel/themes/cookies</a> et <a href="https://www.allaboutcookies.org/fr/">https://www.allaboutcookies.org/fr/</a>.
</p>
